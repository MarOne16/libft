#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*l;

	if (!lst)
		return (0x0);
	l = lst;
	while (l -> next != NULL)
	{
		l = l -> next;
	}
	return (l);
}
